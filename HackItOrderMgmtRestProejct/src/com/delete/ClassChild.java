package com.delete;

public class ClassChild {
	private String name;
	private String value;
	
	public ClassChild(){
		
	}
	public ClassChild(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}
}
